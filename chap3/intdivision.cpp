#include <iostream>

int main()
{
    std::cout << "Result for integer division 13/4:" << std::endl;
    std::cout << 13 / 4 << std::endl;
    std::cout << "Result of floating point division 13.0/4.0:" << std::endl;
    std::cout << 13.0 / 4.0 << std::endl;
    std::cout << "Results of mixed division 13.0/4:" << std::endl;
    std::cout << 13.0 / 4 << std::endl;
    return 0;
}
